package com.werzuo.shfcms.model;

/**
 *
 * @author Deshaa
 */
public class PaymentType {

    private String ptId;
    private String type;
    private double fee;

    public PaymentType() {
    }

    public PaymentType(String ptId, String type, double fee) {
        this.ptId = ptId;
        this.type = type;
        this.fee = fee;
    }

    public String getPtId() {
        return ptId;
    }

    public void setPtId(String ptId) {
        this.ptId = ptId;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    /**
     * @return the fee
     */
    public double getFee() {
        return fee;
    }

    /**
     * @param fee the fee to set
     */
    public void setFee(double fee) {
        this.fee = fee;
    }

}
