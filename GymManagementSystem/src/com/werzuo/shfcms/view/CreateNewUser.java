    /**
     * create a new user
     *
     * @throws SQLException
     * @throws ClassNotFoundException
     */
    private void createNewUser() throws SQLException, ClassNotFoundException {

        String name = txtFname.getText();
        char[] passwordTemp1 = txtPasword.getPassword();
        char[] passwordTemp2 = txtRePassword.getPassword();
        String password1 = String.valueOf(passwordTemp1);
        String password2 = String.valueOf(passwordTemp2);
        String type = comboType.getSelectedItem().toString();

        if (!password1.equals(password2)) {
            JOptionPane.showMessageDialog(this, "passwords are incorrect !");
        } else {
            User user = new User(type, name, password2, type);
            UserController userController = new ControllerFactoryImpl().getUserController();
            boolean addUser = userController.addUser(user);
            if (addUser) {
                new OptionPaneAddedSuccess(homeFrame, true, "User Creation Success !");
                setEmphtyFields();
            } else {
                new OptionPaneAddedSuccess(homeFrame, true, "User Creation Failed !");
            }

        }
    }

    /**
     * find out a user
     */
    private void findUser() throws SQLException, ClassNotFoundException {

        nameT = txtFname.getText();
        UserController userController = new ControllerFactoryImpl().getUserController();
        User user = userController.searchUser(nameT);
        if (user != null) {
            String type = user.getType();
            String name = user.getName();
            String password = user.getPassword();

            txtPasword.setText(password);
            txtFname.setText(name);

            if ("Admin".equalsIgnoreCase(type)) {
                comboType.setSelectedIndex(0);
            } else {
                comboType.setSelectedIndex(1);
            }
        } else {
            new OptionPaneAddedSuccess(homeFrame, true, "Sorry No User Found For" + nameT + " !");
            setEmphtyFields();
        }
    }

    /**
     * remove a user from database
     */
    private void removeUser() throws SQLException, ClassNotFoundException {
        String name = txtFname.getText();
        UserController userController = new ControllerFactoryImpl().getUserController();
        boolean deleteUser = userController.deleteUser(name);

        if (deleteUser) {
            new OptionPaneAddedSuccess(homeFrame, true, "User Removed Success !");
            setEmphtyFields();
        } else {
            new OptionPaneAddedSuccess(homeFrame, true, "User Remove Failed !");
        }
    }

    /**
     * modify the user
     */
    private void modifyUser() throws SQLException, ClassNotFoundException {

        char[] passwordTemp1 = txtPasword.getPassword();
        char[] passwordTemp2 = txtRePassword.getPassword();
        String password1 = String.valueOf(passwordTemp1);
        String password2 = String.valueOf(passwordTemp2);
        String type = comboType.getSelectedItem().toString();

        if (!password1.equals(password2)) {
            JOptionPane.showMessageDialog(this, "passwords do not match !");
            setEmphtyFields();
        } else {
            User user = new User(type, nameT, password2, type);
            UserController userController = new ControllerFactoryImpl().getUserController();
            boolean updateUser = userController.updateUser(user);
            if (updateUser) {
                new OptionPaneAddedSuccess(homeFrame, true, "User Modification Success !");
                setEmphtyFields();
            } else {
                new OptionPaneAddedSuccess(homeFrame, true, "User Modification Failed !");
            }

        }

    }

    /**
     * create the text fields empty
     */
    private void setEmphtyFields() {
        txtFname.setText(null);
        txtPasword.setText(null);
        txtRePassword.setText(null);
    }
