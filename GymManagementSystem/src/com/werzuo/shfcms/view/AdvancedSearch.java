    private void searchMember() throws SQLException, ClassNotFoundException {
        String search = txtSearch.getText();
        ArrayList<Member> advancedSearchMembers
                = memberController.getAdvancedSearchMembers(search);

        dtm.setRowCount(0);
        for (Member advancedSearchMember : advancedSearchMembers) {
            String name = advancedSearchMember.getfName() + " " + advancedSearchMember.getlName();

            Object[] row = {advancedSearchMember.getCid(), name, advancedSearchMember.getNic(), advancedSearchMember.getGender()};
            dtm.addRow(row);
        }
    }

    public void loadMembers() {
        try {
            memberController = new ControllerFactoryImpl().getMemberController();
            ArrayList<Member> allMembers = memberController.getAllMember();
            if (allMembers != null) {
                dtm = (DefaultTableModel) jTable1.getModel();
                dtm.setRowCount(0);
                for (Member member : allMembers) {
                    String name = member.getfName() + " " + member.getlName();

                    Object[] row = {member.getCid(), name, member.getNic(), member.getGender()};
                    dtm.addRow(row);
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(AdvancedSearch.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AdvancedSearch.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
